Programa da disciplina:
Dia 01:
  •	Manipulação e visualização de objetos espaciais no R.
  •	Aquisição e preparo de dados de biodiversidade.
  • Mapas de calor.

Dia 02:
  •	Estimando área (AOO) e extensão (EOO) de ocorrência de espécies.
  •	Estimando métricas de biodiversidade a partir de matrizes de presença-ausência.
  •	Beta-diversidade e outras métricas de dissimilaridade entre sites.

Dia 03
  •	Aquisição e preparo de dados ambientais (clima, solo, etc.).
  •	Interpolação
  • Análises de correlação e PCA com variáveis ambientais
  • Extraindo informações de variáveis ambientais

Dia 04:
  •	Dualidade de Hutchinson: Espaço geográfico vs Espaço ambiental.
  •	Autocorrelação espacial e índice de Moran.
  •	Introdução à modelagem de nicho ecológico.

Dia 05:
  •	Associação de dados de biodiversidade com dados de paisagem.
  •	Geração de mapas para publicação.
